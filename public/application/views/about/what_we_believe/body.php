     <h1>
      What We Believe
     </h1>
     <p>&nbsp;
      
     </p>
     <div id="image3"><img src="../images/CIT-Believe.jpg" width="320" height="210" alt="believe" /></div>
     <h2>
      The Scriptures
     </h2>
     <p>
      We believe that the Holy Bible, consisting of the Old and New Testaments, is the verbally inspired and infallible Word of God, and is the final authority in faith and life.
     </p>
     <br />
     <h2>
      God
     </h2>
     <p>
      We believe in one, true and living God, eternally existing in three persons, the Father, the Son, and the Holy Spirit, who are co-equal. God the Father Almighty is the Creator of heaven and earth, and of all things visible and invisible.
     </p>
     <br />
     <h2>
      Jesus Christ
     </h2>
     <p>
      We believe in Jesus Christ, the only begotten Son of God, who is our Lord. He was conceived of the Holy Spirit and born of the Virgin Mary to be a sinless man; in Him perfect humanity and deity are found in one person. He is the one mediator between God and mankind. He was crucified on the cross for the sins of all mankind. He rose from the dead, ascended to heaven and was made Lord. He will come again to judge the living and the dead.
     </p>
     <br />
     <h2>
      Holy Spirit
     </h2>
     <p>
      We believe in the Holy Spirit, the Divine Person who convicts the world of sin, of righteousness, and of judgment. In Him all believers were baptized into the Body of Christ. He indwells them, transforms their lives and continues to guide, shepherd, teach, and comfort them.
     </p>
     <br />
     <h2>
      Mankind
     </h2>
     <p>
      We believe that mankind was created in the image and likeness of God, but due to Adam's sin, all mankind inherited a sinful nature. All mankind has sinned and come short of the glory of God, becoming spiritually dead and alienated from Him. Therefore each human being needs to repent of their sins and believe in Jesus Christ.
     </p>
     <br />
     <h2>
      Salvation
     </h2>
     <p>
      We believe that salvation depends solely on the finished work of Jesus Christ, who accomplished redemption once and for all on the cross at Calvary as the vicarious sacrifice for the sins of all mankind. All those who put their faith in Jesus Christ and His work, believing that God has raised Him from the dead, and confess Him as their Lord, shall be saved forever and have eternal life. This salvation is the gift of God's grace and is not gained by human works of merit.
     </p>
